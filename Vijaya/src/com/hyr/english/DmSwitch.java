package com.hyr.english;

public class DmSwitch {
	public static void main(String args[]) {
		int i = 5;
		switch(i) {
		case 1:
			System.out.println("this is the first statement");
			//statements
			break;
		case 2:
			System.out.println("this is the second statement");
			//statements
			break;
		case 3:
			System.out.println("this is the 3 statement");
			
			//statements
			break;
			default:
			System.out.println("default");
			break;
		}
	}
	

}
