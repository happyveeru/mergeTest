package com.broadridge.bank;

public class Switchstate {
	
	public static void main(String args[]) {
		int age;
		age = 18;
		
		switch (age) {
		case 18:
		
		System.out.println("you can vote now");
		break;
		
		case 25:
		System.out.println("you can marry now");
		break;
		
		case 60:
		System.out.println("you can rest now");
		
		
		
		}
		
	}

}
